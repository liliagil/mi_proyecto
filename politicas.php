    <p>FPOLÍTICA DE TRATAMIENTO DE LA INFORMACIÓN

CRONOGAS PYG NIT 28548678-8

De acuerdo con lo dispuesto en la Constitución Política de Colombia, la Ley 1581 de 2012, el Decreto 1377 de 2013, y demás normas que las modifiquen, deroguen o sustituyan, los datos personales suministrados a la empresa CRONOGAS PYG serán incorporados a una base de datos denominada “BASE DE DATOS DE CLIENTES” de la que es responsable CRONOGAS PYGcon domicilio Calle 2A 16-46 Barrio minuto de Dios 3 etp.

La finalidad de la BASE DE DATOS DE CLIENTES es el: i) mantenimiento, desarrollo y gestión de la relación comercial entre la empresa y el titular de los datos personales; ii) el envío de comunicaciones comerciales de los productos, ofertas y/o beneficios de la empresa y de las empresas colaboradoras de ésta, quienes prestan servicios y otros, por consiguiente se requiere obtener su autorización para que de manera libre, previa, expresa, voluntaria y debidamente informada, permita a la empresa recolectar, registrar, almacenar, usar, suprimir, procesar, compilar, dar tratamiento, actualizar y disponer de los datos que han sido suministrados.

La BASE DE DATOS DE CLIENTES estará vigente mientras la actividad comercial de la empresa se encuentre en operación.

CRONOGAS PYG está comprometida a dar un correcto uso y tratamiento de los datos personales de sus Clientes, evitando el acceso no autorizado a terceros que permita conocer o vulnerar, modificar, divulgar y/o destruir la información que reposa en las bases de datos de la empresa. Por tal motivo, CRONOGAS PYG cuenta con protocolos de seguridad y acceso a sus sistemas de información, almacenamiento y procesamiento, incluidas medidas físicas de control de riesgos de seguridad.

El Área Administrativa será la encargada y responsable de la BASE DE DATOS DE CLIENTES.

Los titulares de los datos personales recogidos en la BASE DE DATOS DE CLIENTES tendrán derecho, de forma gratuita a: conocer, actualizar, rectificar, solicitar que sus datos sean retirados, así como todos aquellos que le otorgue la ley. Para lo cual deberá realizar una comunicación, y que podrá entregar en las oficinas del domicilio principal de la empresa, a través del portal web o de manera verbal en la línea de atención 3208899901.

Los derechos de los Titulares podrán ejercerse por: el Titular, por sus causahabientes, (quienes deberán acreditar tal calidad), por el representante y/o apoderado del Titular.

La empresa se reserva el derecho a modificar, en cualquier momento, de manera unilateral, la Política de Tratamiento de la Información. No obstante, la Política de Tratamiento de la Información vigente en cada momento estará disponible en la página web y en las oficinas del domicilio principal de la empresa. Cualquier cambio sustancial en la Política de Tratamiento de la Información que pueda afectar el contenido de la autorización otorgada por el titular será comunicado a éste en los términos establecidos por la normativa vigente. Además, se conservarán las versiones anteriores de la Política de Tratamiento de la Información.<P>

